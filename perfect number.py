def perfect_number(n):
    a=0
    for i in range(1,n):
        if(n%1==0):
            a=a+i
    return a
n=4
perfect_number(n)
if(n==perfect_number(n)):
    print("{}is the perfect number".format(n))
else:
    print("{}not a perfect number".format(n))

